    def get_hexagram_by_number(self, number):
        return self.hexagrams[number]

    def get_hexagram_by_name(self, name):
        for hexagram in self.hexagrams:
            if hexagram.name == name:
                return hexagram
        return None

    def get_hexagram_by_symbol(self, symbol):
        for hexagram in self.hexagrams:
            if hexagram.symbol == symbol:
                return hexagram
            
    @staticmethod
    def generate_random_hexagram():
        return random.choice(IChing.HEXAGRAMS)
    
    @staticmethod
    def search_hexagram(keyword):
        results = []
        for hexagram in IChing.HEXAGRAMS:
            if keyword in hexagram.name or keyword in hexagram.symbol or keyword in hexagram.mnemonic:
                results.append(hexagram)
        return results
    
    
    